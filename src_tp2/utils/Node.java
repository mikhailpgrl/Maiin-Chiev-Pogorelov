package utils;

public class Node {
	
	public int page;
	public int numberPage;
	
	public Node(int page, int numberPage){
		this.page = page;
		this.numberPage = numberPage;
	}

	public int getPage() {
		return page;
	}

	public void setPage(int page) {
		this.page = page;
	}

	public int getNumberPage() {
		return numberPage;
	}

	public void setNumberPage(int numberPage) {
		this.numberPage = numberPage;
	}
	
	

}
